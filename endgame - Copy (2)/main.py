def generate_ppt():
    """Generate a PowerPoint presentation from voice input."""
    speak("Please say the topic for your PowerPoint presentation.")
    topic = command()
    
    if not topic:
        speak("No topic detected. Please try again.")
        return

    speak(f"Generating a presentation on {topic}. Please wait.")
    
    try:
        # Generate content using Gemini AI
        prompt = f"Create an educational PowerPoint outline on {topic}. Include a title slide and 4-5 key slides with summarized points."
        response = model.generate_content(prompt)
        slides_content = response.text.split("\n\n")  # Split text into slides

        # Create PowerPoint
        ppt = Presentation()

        # Title Slide
        slide_layout = ppt.slide_layouts[0]
        slide = ppt.slides.add_slide(slide_layout)
        title = slide.shapes.title
        subtitle = slide.placeholders[1]
        title.text = f"{topic}"
        subtitle.text = "Generated by Jarvis AI"

        # Add Content Slides
        for slide_text in slides_content:
            slide_layout = ppt.slide_layouts[1]  # Title & Content Layout
            slide = ppt.slides.add_slide(slide_layout)
            title, content = slide.shapes.title, slide.placeholders[1]

            slide_parts = slide_text.split(": ", 1)
            if len(slide_parts) == 2:
                title.text, content.text = slide_parts
            else:
                title.text, content.text = topic, slide_text

        # Save the presentation
        ppt_filename = f"{topic.replace(' ', '_')}.pptx"
        ppt.save(ppt_filename)

        speak(f"Presentation on {topic} has been created successfully. Opening the file now.")
        os.startfile(ppt_filename)

    except Exception as e:
        speak("I encountered an error while generating the presentation.")
        print("Error:", e)

