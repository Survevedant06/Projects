class FaceRecognitionThread(QThread):
    update_frame = QtCore.pyqtSignal(QImage)
    authentication_result = QtCore.pyqtSignal(bool)
    
    def __init__(self, reference_image_path):
        super().__init__()
        self.reference_image_path = reference_image_path
        self.running = True
        
    def run(self):
        # Load face cascade for simple face detection - no dlib required
        face_cascade = cv2.CascadeClassifier(cv2.data.haarcascades + 'haarcascade_frontalface_default.xml')
        
        # Access webcam (use camera index 1 for DroidCam)
        cap = cv2.VideoCapture(1)  # Changed from 0 to 1 for DroidCam
        if not cap.isOpened():
            print("Cannot open camera")
            self.authentication_result.emit(False)
            return
            
        face_detected_frames = 0
        required_frames = 20  # Need to detect face in 20 frames to authenticate
        attempts = 0
        max_attempts = 100  # About 10 seconds of trying
        
        while self.running and attempts < max_attempts:
            ret, frame = cap.read()
            if not ret:
                print("Can't receive frame. Exiting...")
                break
                
            # Convert to grayscale for face detection
            gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
            
            # Detect faces
            faces = face_cascade.detectMultiScale(gray, 1.3, 5)
            
            # Add visual effects to frame
            # Add a border
            frame = cv2.copyMakeBorder(frame, 10, 10, 10, 10, cv2.BORDER_CONSTANT, value=[0, 120, 255])
            
            # Add scanning effect overlay
            overlay = frame.copy()
            # Draw horizontal scan line
            scan_position = (attempts % 50) * (frame.shape[0] // 50)
            cv2.line(overlay, (0, scan_position), (frame.shape[1], scan_position), (0, 255, 0), 2)
            frame = cv2.addWeighted(overlay, 0.7, frame, 0.3, 0)
            
            # Add system status text
            cv2.putText(frame, "JARVIS SECURITY SYSTEM", (frame.shape[1]//2 - 150, 30), 
                      cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 255), 2)
            
            if len(faces) > 0:
                # Face detected
                face_detected_frames += 1
                
                # Draw rectangle around face
                for (x, y, w, h) in faces:
                    # Draw fancy rectangle
                    cv2.rectangle(frame, (x, y), (x+w, y+h), (0, 255, 0), 2)
                    
                    # Draw corner lines for futuristic look
                    corner_length = 20
                    # Top left
                    cv2.line(frame, (x, y), (x + corner_length, y), (0, 255, 255), 2)
                    cv2.line(frame, (x, y), (x, y + corner_length), (0, 255, 255), 2)
                    # Top right
                    cv2.line(frame, (x+w, y), (x+w - corner_length, y), (0, 255, 255), 2)
                    cv2.line(frame, (x+w, y), (x+w, y + corner_length), (0, 255, 255), 2)
                    # Bottom left
                    cv2.line(frame, (x, y+h), (x + corner_length, y+h), (0, 255, 255), 2)
                    cv2.line(frame, (x, y+h), (x, y+h - corner_length), (0, 255, 255), 2)
                    # Bottom right
                    cv2.line(frame, (x+w, y+h), (x+w - corner_length, y+h), (0, 255, 255), 2)
                    cv2.line(frame, (x+w, y+h), (x+w, y+h - corner_length), (0, 255, 255), 2)
                    
                    progress = min(face_detected_frames / required_frames, 1.0)
                    bar_width = int(w * progress)
                    
                    # Draw progress bar
                    cv2.rectangle(frame, (x, y+h+5), (x+bar_width, y+h+15), (0, 255, 0), -1)
                    cv2.rectangle(frame, (x, y+h+5), (x+w, y+h+15), (255, 255, 255), 1)
                    
                    # Progress percentage
                    percentage = int(progress * 100)
                    cv2.putText(frame, f"Scanning: {percentage}%", (x, y+h+30), 
                              cv2.FONT_HERSHEY_SIMPLEX, 0.6, (0, 255, 0), 2)
                
                # Check if we have enough consistent detections
                if face_detected_frames >= required_frames:
                    # Authentication successful
                    # Create success overlay
                    success_overlay = frame.copy()
                    cv2.rectangle(success_overlay, (0, 0), (frame.shape[1], frame.shape[0]), (0, 255, 0), -1)
                    
                    # Blend with original frame
                    frame = cv2.addWeighted(success_overlay, 0.3, frame, 0.7, 0)
                    
                    # Add success text
                    cv2.putText(frame, "ACCESS GRANTED", (frame.shape[1]//2 - 150, frame.shape[0]//2), 
                              cv2.FONT_HERSHEY_SIMPLEX, 1.5, (255, 255, 255), 3)
                    cv2.putText(frame, "AUTHENTICATION SUCCESSFUL", (frame.shape[1]//2 - 200, frame.shape[0]//2 + 50), 
                              cv2.FONT_HERSHEY_SIMPLEX, 1, (255, 255, 255), 2)
                    
                    # Convert frame to QImage and emit
                    h, w, ch = frame.shape
                    qt_image = QImage(frame.data, w, h, ch * w, QImage.Format_RGB888)
                    self.update_frame.emit(qt_image.rgbSwapped())
                    
                    # Wait a moment to display the success message
                    time.sleep(1.5)
                    
                    # Authentication successful - speak confirmation
                    speak("Authentication successful. Welcome back, sir.")
                    
                    # Authentication successful
                    cap.release()
                    self.authentication_result.emit(True)
                    return
            else:
                # Reset counter if face is lost
                face_detected_frames = max(0, face_detected_frames - 1)
                # Warning text
                cv2.putText(frame, "No face detected", (50, 50), cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 0, 255), 2)
                cv2.putText(frame, "Please position your face in the camera view", (50, 80), 
                          cv2.FONT_HERSHEY_SIMPLEX, 0.6, (0, 0, 255), 2)
            
            # Add scanning line effect
            
            # Convert frame to QImage and emit
            h, w, ch = frame.shape
            qt_image = QImage(frame.data, w, h, ch * w, QImage.Format_RGB888)
            self.update_frame.emit(qt_image.rgbSwapped())
            
            attempts += 1
            time.sleep(0.1)
            
        # If we get here without successful authentication
        cap.release()
        speak("Authentication failed. Access denied.")
        self.authentication_result.emit(False)
    
    def stop(self):
        self.running = False
        self.wait()

